function mudouTamanho(){
    if (window.innerWidth >= 670) {
        navmenu.style.display = 'block'
    } else {
        navmenu.style.display ='none'
    }
}


function menuClick() {
    if (navmenu.style.display == 'none') {
        navmenu.style.display = 'block'
    } else {
        navmenu.style.display = 'none'
    }
}